package Storage.src;

public enum AssortmentCodes {

    /*
    GA = General Assortment, used for items that every store should have.
    EA = Expired Assortment, used for items that is no longer being sold by the store.
    UA = Unavailable Assortment, used for items that are not available from the supplier.
    CA = Campaign Assortment, used for items that are to be ordered and sold during this campaign.
    OA = Online Assortment, used for items available only online.
    FA = Flagship Assortment, used for stores with an extended item range, that can hold more items than regular stores.
    */

    GA, EA, UA, CA, OA, FA;

}
